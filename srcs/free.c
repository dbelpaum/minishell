#include "../inc/minishell.h"

void	free_cmd_tab(t_redi **r)
{
	int	i;

	i = 0;
	while (r[i])
	{
		if (r[i]->idin == 2)
			free(r[i]->here_doc);
		free(r[i]);
		i++;
	}
	free(r);
}
